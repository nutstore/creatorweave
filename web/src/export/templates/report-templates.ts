/**
 * Report Templates for PDF Export
 *
 * Pre-built templates for different report types:
 * - Code Review Report
 * - Test Generation Report
 * - Project Analysis Report
 * - Documentation Report
 *
 * @module report-templates
 */

import type {
  PDFExportOptions,
  CodeReviewReportData,
  TestGenerationReportData,
  ProjectAnalysisReportData,
} from '../pdf-export'

// ============================================================================
// Template Registry
// ============================================================================

export type TemplateType = 'code-review' | 'test-report' | 'project-analysis' | 'doc-generation'

export interface TemplateConfig {
  type: TemplateType
  name: string
  description: string
  generateHTML: (data: unknown) => string
  getDefaultOptions: () => Partial<PDFExportOptions>
}

