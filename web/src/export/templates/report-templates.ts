/**
 * Render issues grouped by category
 */
function renderIssuesByCategory(issues: CodeReviewReportData['issues']): string {
  const categories: Record<string, typeof issues> = {
    security: issues.filter((i: (typeof issues)[0]) => i.category === 'security'),
    performance: issues.filter((i: (typeof issues)[0]) => i.category === 'performance'),
    style: issues.filter((i: (typeof issues)[0]) => i.category === 'style'),
    'best-practice': issues.filter((i: (typeof issues)[0]) => i.category === 'best-practice'),
  }

  const categoryNames: Record<string, string> = {
    security: 'Security Issues',
    performance: 'Performance Issues',
    style: 'Style Issues',
    'best-practice': 'Best Practices',
  }

  return Object.entries(categories)
    .filter(([, items]) => items.length > 0)
    .map(
      ([category, items]) => `
      <div class="section">
        <h2>${categoryNames[category] || category} (${items.length})</h2>
        ${items
          .map(
            (issue) => `
          <div class="issue ${issue.severity}">
            <div class="issue-header">
              <span class="issue-title">${escapeHtml(issue.message)}</span>
              <span class="badge ${issue.severity}">${issue.severity}</span>
            </div>
            <div class="issue-meta">
              <span class="issue-location">Line ${issue.line}:${issue.column}</span>
              <span style="margin-left: 8px;">${issue.rule}</span>
            </div>
            ${
              issue.suggestion
                ? `
              <div class="issue-suggestion">
                <strong>Suggestion:</strong> ${escapeHtml(issue.suggestion)}
              </div>
            `
                : ''
            }
          </div>
        `
          )
          .join('')}
      </div>
    `
    )
    .join('')
}

