export const templates: Record<TemplateType, TemplateConfig> = {
  'code-review': codeReviewTemplate,
  'test-report': testReportTemplate,
  'project-analysis': projectAnalysisTemplate,
  'doc-generation': {
    type: 'doc-generation',
    name: 'Documentation Report',
    description: 'Generated documentation summary',
    generateHTML: () => '<div>Documentation template</div>',
    getDefaultOptions: () => ({
      title: 'Documentation',
      includeTOC: true,
      pageNumbers: true,
      header: 'Documentation',
      footer: 'Generated by AI Workspace',
    }),
  },
}

/**
 * Get template by type
 */
export function getTemplate(type: TemplateType): TemplateConfig | undefined {
  return templates[type]
}

/**
 * Get all available templates
 */
export function getAllTemplates(): TemplateConfig[] {
  return Object.values(templates)
}

/**
 * Apply template to data
 */
export function applyTemplate<T>(type: TemplateType, data: T): string {
  const template = getTemplate(type)
  if (!template) {
    throw new Error(`Unknown template type: ${type}`)
  }
  return template.generateHTML(data)
}

// ============================================================================
// Utility
// ============================================================================

function escapeHtml(text: string): string {
  const div = document.createElement('div')
  div.textContent = text
  return div.innerHTML
}
